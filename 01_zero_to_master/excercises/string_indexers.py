# Guess the output of each print statement before you click RUN!
python = 'I am PYHTON'

# python[start : stop : step over]

print(python[1:4])  # ' am'
print(python[2:6])  # 'am P'
print(python[1:])  # ' am PYTHON'
print(python[:])  # 'I am PYHON'
print(python[1:100])  # ?

print(python[-1])  # N
print(python[-4])  # H
print(python[:-3])  # 'I am PYHT'
print(python[-3:])  # TON
print(python[::-1])  # Reverse
